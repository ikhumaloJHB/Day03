#include <unistd.h>

void  ft_ultimate_div_mod(int *a,int *b){

	int c;
	int d;
	int e;
	int f;
     e = *a;
     f = *b;		 
	c = e / f;
	d = e % f;

	a = &c;
	b = &d;
}	
	

