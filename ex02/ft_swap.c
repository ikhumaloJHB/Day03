#include <unistd.h>


void ft_swap(int *a, int *b){

     int *temp;
     int *temp2;
      	 
       temp = a;
	   temp2 = b;

       a = temp2;
	   b = temp	;  
}

int main(){

	int *n;
	int *m;

	ft_swap(n,m);
}


